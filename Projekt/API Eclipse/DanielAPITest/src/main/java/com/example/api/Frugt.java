package com.example.api;

public class Frugt {
	
	int Id;
	String name;
	String desc;
	int weight;
	String country;
	
	public Frugt() {}
	
	public Frugt(int Id, String name, String desc, int weight, String country) {
		super();
		this.name = name;
		this.desc = desc;
		this.weight = weight;
		this.country = country;
	}

	public int getId() {
		return Id;
	}

	public void setId(int id) {
		Id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

	public int getWeight() {
		return weight;
	}

	public void setWeight(int weight) {
		this.weight = weight;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}
	
	
	
}
